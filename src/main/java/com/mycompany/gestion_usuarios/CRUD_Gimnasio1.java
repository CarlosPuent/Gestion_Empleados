    private void btnEliminarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnEliminarMouseClicked
        try {
            int index = Tabla1.getSelectedRow();

            if (index >= 0) {
                TableModel modelo = Tabla1.getModel();
                String id = modelo.getValueAt(index, 0).toString();

                UsuarioGimnasio g = new UsuarioGimnasio();
                g.EliminarUsuario(id);

                g.MostrarUsuarios(Tabla1);
            } else {
                JOptionPane.showMessageDialog(null, "Por favor selecciona un registro para eliminar de la tabla.");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Ocurrió un error al intentar eliminar el registro: " + e.getMessage());
        }
    }//GEN-LAST:event_btnEliminarMouseClicked

