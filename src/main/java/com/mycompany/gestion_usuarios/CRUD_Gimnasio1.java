                TextCorreo.setText(correo);
                TextPENombre.setText(penombre);
                TextPEApellido.setText(peapellido);
                TextParentesco.setText(parentesco);
                JFechaNacimiento.setDate(fechaNacimiento);
                JFechaIngreso.setDate(fechaIngreso);
                JFechaPago.setDate(fechaProximaPago);
